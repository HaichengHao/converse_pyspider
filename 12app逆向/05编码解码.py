# abst:常用于md5加密 salt,AES加密的key,iv


str1 = "天空好像下雨，我好想住你隔壁"
data_bytes = str1.encode('utf-8')
print(data_bytes)
# b'\xe5\xa4\xa9\xe7\xa9\xba\xe5\xa5\xbd\xe5\x83\x8f\xe4\xb8\x8b\xe9\x9b\xa8\xef\xbc\x8c\xe6\x88\x91\xe5\xa5\xbd\xe6\x83\xb3\xe4\xbd\x8f\xe4\xbd\xa0\xe9\x9a\x94\xe5\xa3\x81'

#解码
# tips:创建字符数组
data_bytes_lst = bytearray()

for data_ in data_bytes:
    data_bytes_lst.append(data_)
print(data_bytes_lst)
#tips:解码
result_ = data_bytes_lst.decode('utf-8')
print(result_)

